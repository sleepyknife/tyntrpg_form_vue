}

function insertStringIntoHash(hash: string, str: string, positions: number[]): string {
	let result = "";
	let strIndex = 0;
	for (let i = 0; i < hash.length; i++) {
		// å¦‚æœå½“å‰ç´¢å¼•æ˜¯æŒ‡å®šä½ç½®ä¹‹ä¸€ï¼Œåˆ™ä½¿ç”¨"taoyuantrpg"ä¸­çš„å­—ç¬¦æ›¿æ¢
		if (positions.includes(i) && strIndex < str.length) {
			result += str[strIndex];
			strIndex++;
		} else {
			result += hash[i];
		}
	}
	// å¦‚æœ"taoyuantrpg"è¿˜æœ‰å‰©ä½™å­—ç¬¦ï¼Œå°†å®ƒä»¬è¿½åŠ åˆ°ç»“æœçš„æœ«å°¾
